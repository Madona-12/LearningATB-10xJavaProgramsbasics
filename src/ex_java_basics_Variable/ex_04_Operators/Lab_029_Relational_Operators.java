package ex_04_Operators;

public class Lab_029_Relational_Operators {
    public static void main(String[] args) {
        // Relational operators are
        // >
        // <
        // >=
        // <=
        // !=
        // ==

        int a = 10;
        int b = 20;
        int c =10;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=c);
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a==c);

    }



}
