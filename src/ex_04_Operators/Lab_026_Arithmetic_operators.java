package ex_04_Operators;

public class Lab_026_Arithmetic_operators {
    public static void main(String[] args) {
        int a=20;
        int b=3;
        float c =3f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c); // float variable should display  the output as float value
    }
}
